package PracticeIT_Cap2_PrimitiveData_DefiniteLoops;

public class BlastOff {

    public static void main(String[] args) {

        System.out.println("T-minus ");

        for (int i = 5; i>=1;i--){

            System.out.println(i + ", ");
        }

        System.out.println("BlastOff!");
    }
}

package PracticeIT_Cap2_PrimitiveData_DefiniteLoops;

public class Multiplication2 {

    public static void main(String[] args) {

        for (int i = 5 ; i>=1 ; i--) {

            for (int j = 10 ; j >=1;j--) {

                System.out.print( (i * j ) + " " );
            }

            System.out.println();
        }
    }
}

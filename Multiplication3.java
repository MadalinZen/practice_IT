package PracticeIT_Cap2_PrimitiveData_DefiniteLoops;

public class Multiplication3 {

    public static void main(String[] args) {

        for (int j = 1 ; j <=5;j++) {

            for (int i = 10 ; i>=1 ; i--) {

                System.out.print( (i * j ) + " " );
            }

            System.out.println();
        }
    }
}

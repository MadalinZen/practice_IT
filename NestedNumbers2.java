package PracticeIT_Cap2_PrimitiveData_DefiniteLoops;

public class NestedNumbers2 {


    public static void main(String[] args) {

        for (int q = 0;q<5;q++) {

            for (int s = 0; s < 1; s++) {

                System.out.print("99999");

                for (int r = 0; r < 1; r++) {

                    System.out.print("88888");

                    for (int i = 0; i < 1; i++) {

                        System.out.print("77777");

                        for (int j = 0; j < 1; j++) {

                            System.out.print("66666");

                            for (int k = 0; k < 1; k++) {

                                System.out.print("55555");

                                for (int l = 0; l < 1; l++) {

                                    System.out.print("44444");

                                    for (int m = 0; m < 1; m++) {

                                        System.out.print("33333");

                                        for (int n = 0; n < 1; n++) {

                                            System.out.print("22222");

                                            for (int o = 0; o < 1; o++) {

                                                System.out.print("11111");

                                                for (int p = 0; p < 1; p++) {
                                                    System.out.println("00000");
                                                }
                                            }
                                        }
                                    }
                                }
                            }
                        }
                    }

                }

            }
        }

    }
}

public class StarFigure {

    public static void main(String[] args) {

        System.out.println("////////////////\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\");

        for (int i = 0 ; i <1;i++){

            System.out.println("////////////********\\\\\\\\\\\\\\\\\\\\\\\\");

            for (int j = 0 ; j < 1;j++){

                System.out.println("////////****************\\\\\\\\\\\\\\\\");

                for (int k = 0 ; k < 1 ;k++){

                    System.out.println("////************************\\\\\\\\");

                    for (int m = 0 ; m<1;m++){

                        System.out.println("********************************");
                    }
                }
            }
        }
    }
}

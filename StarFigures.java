package PracticeIT_Cap1_Intro;

public class StarFigures {

    public static void main(String[] args) {

        firstStar();
        System.out.println();
        firstStar();
        secondStar();
        firstStar();

    }

    public static void firstStar(){

        System.out.println("*****\n" +
                "*****\n" +
                " * *\n" +
                "  *\n" +
                " * *");
    }

    public static void secondStar(){

        System.out.println("*****\n" +
                "*****\n" +
                "\n" +
                "  *\n" +
                "  *\n" +
                "  *");
    }


}

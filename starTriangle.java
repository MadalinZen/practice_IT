package PracticeIT_Cap2_PrimitiveData_DefiniteLoops;

public class starTriangle {

    public static void main(String[] args) {

        for (int i = 0 ; i < 1; i++){

            System.out.println("*");

            for (int j = 0 ; j < 1 ; j++ ){

                System.out.println("**");

                for (int m = 0 ; m < 1 ; m++){

                    System.out.println("***");

                    for (int p = 0 ; p < 1 ; p++){

                        System.out.println("****");

                        System.out.println("*****");
                    }
                }
            }
        }


    }
}
